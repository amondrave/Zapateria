    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        // TODO add your handling code here:
        er1.setText("");
        er2.setText("");
        er3.setText("");
        er4.setText("");
        er5.setText("");
        
        int k=0; boolean vacioSn=false; boolean vacioNo=false; long ced=0; double not=0;
        try{
             ced=Long.parseLong(cedula.getText().trim());
        }catch(Exception e){
             JOptionPane.showMessageDialog(null, "Ingrese un N√∫mero", "Error", JOptionPane.ERROR_MESSAGE);  
             return;
        }
        if(cedula.getText().isEmpty()){
            er1.setText("!");
            k++;
        }
        if(nombre.getText().isEmpty()){
            er2.setText("!");
            k++;
        }
        if(dir.getText().isEmpty()){
            er3.setText("!");
            k++;
        }
        if(tel.getText().isEmpty()){
            er4.setText("!");
            k++;
        }
        if(ind.getSelectedIndex()==0){
            er5.setText("!");
            k++;
        }
        if(k>0){
           JOptionPane.showMessageDialog(null, "Complete Los Campos Restantes", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            
            c.setCedula(cedula.getText().trim());
            c.setNombre(nombre.getText().trim());
            c.setDireccion(dir.getText().trim());
            c.setTelefono(ind.getSelectedItem().toString()+" "+tel.getText());
            try {
                cd.insertar(c);
                JOptionPane.showMessageDialog(null, "Proceso Exitoso", "Exitoso", JOptionPane.INFORMATION_MESSAGE);
            } catch (DAOException ex) {
                Logger.getLogger(AgregarCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            cedula.setText("");
            nombre.setText("");
            dir.setText("");
            tel.setText("");
            
            ind.setSelectedIndex(0);
            
        } 
        
        er1.setText("");
        er2.setText("");
        er3.setText("");
        er4.setText("");
        er5.setText("");
        
        
        
    }//GEN-LAST:event_agregarActionPerformed

