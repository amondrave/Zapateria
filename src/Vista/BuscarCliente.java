       public void cargarCombo(ArrayList<Cliente> n){
        
        clie.removeAllItems();
        
        clie.addItem("Seleccionar");
        for(Cliente c: n){
         clie.addItem(c.getNombre());
        }
    }
     
    public Cliente buscar(ArrayList<Cliente> n, String m){
        Cliente aux=null;
        for(Cliente c: n){
           if(c.getNombre().equalsIgnoreCase(m)){
              aux=c;
           } 
        }
        return aux;
    }
    
    
    
